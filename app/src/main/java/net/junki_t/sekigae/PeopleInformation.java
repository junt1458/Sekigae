public class PeopleInformation {

    public boolean isMan;
    public int SeatNumber;
    public String Name;
    public int Number;
    public int AllNumber;

    public PeopleInformation(boolean isMan, int SeatNumber, String Name, int Number, int AllNumber){
        this.isMan = isMan;
        this.SeatNumber = SeatNumber;
        this.Name = Name;
        this.Number = Number;
        this.AllNumber = AllNumber;
    }

}
